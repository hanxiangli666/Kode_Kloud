#!/bin/bash
# Git SSH 推送脚本 - 在添加 SSH 公钥到 GitHub 后运行此脚本

# ============================================
# 步骤 1: 测试 SSH 连接
# ============================================
echo "======================================"
echo "测试 SSH 连接到 GitHub..."
echo "======================================"

ssh -T git@github.com

if [ $? -eq 0 ]; then
    echo "✓ SSH 连接成功！"
else
    echo "❌ SSH 连接失败，请确保公钥已添加到 GitHub"
    exit 1
fi

# ============================================
# 步骤 2: 查看当前同步状态
# ============================================
echo ""
echo "======================================"
echo "检查同步状态..."
echo "======================================"

git log --oneline -1
echo "远程最新:"
git log origin/main --oneline -1

# ============================================
# 步骤 3: 拉取最新更改（如果有的话）
# ============================================
echo ""
echo "======================================"
echo "拉取远程更新..."
echo "======================================"

git fetch origin main
git status

# ============================================
# 步骤 4: 推送更改
# ============================================
echo ""
echo "======================================"
echo "推送本地改动到 GitHub..."
echo "======================================"

git push origin main

if [ $? -eq 0 ]; then
    echo ""
    echo "✓✓✓ 推送成功！✓✓✓"
    echo "======================================="
    git log --oneline -1
else
    echo ""
    echo "❌ 推送失败，尝试 force push..."
    git push origin main -f
fi

echo ""
echo "======================================"
echo "完成！"
echo "======================================"
